function normalizeLookupKey(value) {
  return String(value || "")
    .toLowerCase()
    .normalize("NFD")
    .replaceAll("\u0153", "oe")
    .replaceAll("\u00e6", "ae")
    .replace(/[\u0300-\u036f]/gu, "");
}

function buildNormalizedExceptionMap(entries) {
  return new Map(entries.map(([word, syllables]) => [normalizeLookupKey(word), syllables]));
}

